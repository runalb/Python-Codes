a=int(input("Enter 1st value: "))
b=int(input("Enter 2nd value: "))
c=(input("Operator: "))
if(c=="+"):
    print(a+b)
elif(c=="-"):
    print(a-b)
elif(c=="/"):
    print(a/b)
elif (c == "*"):
    print(a*b)
elif(c=="//"):
    print(a//b)
elif(c=="%"):
    print(a%b)
elif(c=="**"):
    print(a**b)
